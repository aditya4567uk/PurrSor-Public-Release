class Point:
    def __init__(self, x, y):
        self.x = x
        self.y = y


class Size:
    def __init__(self, cx, cy):
        self.cx = cx
        self.cy = cy


class Rect:
    def __init__(self, left, top, right, bottom):
        self.top = top
        self.right = right
        self.bottom = bottom
        self.left = left
